local SettingCustomLayoutMap = require("Business/Setting/Proxies/Map/SettingCustomLayoutMap")
local keyList = SettingCustomLayoutMap.KeyList
local customIndex = 80000
local nums = 10
for i = 1, nums do
  local beginIndex = customIndex + (i - 1) * 10000
  for k, v in pairs(keyList) do
    local keyStr, keyStr1, keyStr2
    if k == "MoveLine" then
      keyStr = string.format("%s_%d", k, i)
      if keyMap[keyStr] == nil then
        keyMap[keyStr] = beginIndex + v[3] * 10
      end
    else
      keyStr1 = string.format("%s_%d_1", k, i)
      keyStr2 = string.format("%s_%d_2", k, i)
      if keyMap[keyStr1] == nil then
        keyMap[keyStr1] = beginIndex + v[3] * 10 + 1
      end
      if keyMap[keyStr2] == nil then
        keyMap[keyStr2] = beginIndex + v[3] * 10 + 2
      end
    end
  end
end
local indexMap = {}
local reverseMap = function()
  for k, v in pairs(keyMap) do
    if nil ~= indexMap[v] then
      LogInfo("SettingStoreMap", "you should check the keyMap, because the value is repteatd! the repetaed value is " .. tostring(v))
    end
    indexMap[v] = k
  end
end
reverseMap()
